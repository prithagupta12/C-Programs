/*to find whether a given num is greater than 50 or not*/
#include<stdio.h>
int main()
{
    int a;
    printf("enter a num:");
    scanf("%d",&a);
    if(a>50)
    {
        printf("The num is greater than 50");
    }
    else
    {
        printf("The num is less than 50");
    }
    return 0;
}
